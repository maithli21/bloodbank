CREATE OR REPLACE TRIGGER trg_check_expiry
BEFORE INSERT OR UPDATE ON BLOOD_UNIT
FOR EACH ROW
BEGIN
    IF :NEW.expiry_date < SYSDATE THEN
        :NEW.status := 'Expired';
    END IF;
END;
