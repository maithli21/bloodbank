CREATE OR REPLACE PROCEDURE issue_blood (
    p_unit_id     IN NUMBER,
    p_hospital_id IN NUMBER,
    p_admin_id    IN NUMBER
)
AS
    v_status BLOOD_UNIT.status%TYPE;
BEGIN
    -- Get current status of blood unit
    SELECT status
    INTO v_status
    FROM BLOOD_UNIT
    WHERE unit_id = p_unit_id;

    -- Check if unit is available
    IF v_status != 'Available' THEN
        DBMS_OUTPUT.PUT_LINE('Blood unit is not available');
        RETURN;
    END IF;

    -- Insert into issue record
    INSERT INTO ISSUE_RECORD
    (unit_id, hospital_id, handled_by)
    VALUES
    (p_unit_id, p_hospital_id, p_admin_id);

    -- Update blood unit status
    UPDATE BLOOD_UNIT
    SET status = 'Issued'
    WHERE unit_id = p_unit_id;

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('Blood issued successfully');

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Invalid Unit ID');

    WHEN DUP_VAL_ON_INDEX THEN
        DBMS_OUTPUT.PUT_LINE('This unit is already issued');

    WHEN OTHERS THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('Error while issuing blood');
END;
/
BEGIN
