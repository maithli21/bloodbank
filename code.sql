CREATE OR REPLACE PROCEDURE donor_report
AS
    CURSOR c_donor IS
        SELECT name, blood_group, phone
        FROM DONOR;

    v_name  DONOR.name%TYPE;
    v_group DONOR.blood_group%TYPE;
    v_phone DONOR.phone%TYPE;

BEGIN
    OPEN c_donor;

    LOOP
        FETCH c_donor INTO v_name, v_group, v_phone;
        EXIT WHEN c_donor%NOTFOUND;

        DBMS_OUTPUT.PUT_LINE(
          v_name || ' | ' || v_group || ' | ' || v_phone
        );
    END LOOP;

    CLOSE c_donor;
END;
/
SET SERVEROUTPUT ON

BEGIN
   donor_report;
END;
/









































