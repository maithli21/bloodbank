CREATE TABLE BLOOD_BANK (
    bank_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    bank_name VARCHAR2(100) NOT NULL,
    location VARCHAR2(150),
    contact_no VARCHAR2(15) UNIQUE,
    email VARCHAR2(100) UNIQUE
);

CREATE TABLE DONOR (
    donor_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    age NUMBER CHECK (age BETWEEN 18 AND 65),
    gender VARCHAR2(10) CHECK (gender IN ('Male','Female','Other')),
    blood_group VARCHAR2(5) NOT NULL,
    phone VARCHAR2(15) UNIQUE,
    email VARCHAR2(100),
    address VARCHAR2(200),
    last_donation_date DATE
);

CREATE TABLE USER_ADMIN (
    admin_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(50) UNIQUE NOT NULL,
    password VARCHAR2(100) NOT NULL,
    role VARCHAR2(20) CHECK (role IN ('Admin','Staff')),
    created_at DATE DEFAULT SYSDATE
);

CREATE TABLE HOSPITAL (
    hospital_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hospital_name VARCHAR2(150) NOT NULL,
    address VARCHAR2(200),
    contact_no VARCHAR2(15) UNIQUE,
    email VARCHAR2(100)
);

CREATE TABLE BLOOD_UNIT (
    unit_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    blood_group VARCHAR2(5) NOT NULL,
    collection_date DATE NOT NULL,
    expiry_date DATE NOT NULL,
    status VARCHAR2(20) 
        CHECK (status IN ('Available','Issued','Expired')),
    bank_id NUMBER,
    
    CONSTRAINT fk_unit_bank
    FOREIGN KEY (bank_id)
    REFERENCES BLOOD_BANK(bank_id)
);

CREATE TABLE DONATION_RECORD (
    record_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    donor_id NUMBER NOT NULL,
    unit_id NUMBER NOT NULL,
    donation_date DATE DEFAULT SYSDATE,
    remarks VARCHAR2(200),

    CONSTRAINT fk_dr_donor
    FOREIGN KEY (donor_id)
    REFERENCES DONOR(donor_id),

    CONSTRAINT fk_dr_unit
    FOREIGN KEY (unit_id)
    REFERENCES BLOOD_UNIT(unit_id),

    CONSTRAINT uq_unit_once UNIQUE (unit_id)
);

CREATE TABLE BLOOD_REQUEST (
    request_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hospital_id NUMBER NOT NULL,
    blood_group VARCHAR2(5) NOT NULL,
    quantity NUMBER CHECK (quantity > 0),
    request_date DATE DEFAULT SYSDATE,
    status VARCHAR2(20)
        CHECK (status IN ('Pending','Approved','Rejected')),

    CONSTRAINT fk_br_hospital
    FOREIGN KEY (hospital_id)
    REFERENCES HOSPITAL(hospital_id)
);

CREATE TABLE ISSUE_RECORD (
    issue_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    unit_id NUMBER NOT NULL,
    hospital_id NUMBER NOT NULL,
    issue_date DATE DEFAULT SYSDATE,
    handled_by NUMBER,

    CONSTRAINT fk_ir_unit
    FOREIGN KEY (unit_id)
    REFERENCES BLOOD_UNIT(unit_id),

    CONSTRAINT fk_ir_hospital
    FOREIGN KEY (hospital_id)
    REFERENCES HOSPITAL(hospital_id),

    CONSTRAINT fk_ir_admin
    FOREIGN KEY (handled_by)
    REFERENCES USER_ADMIN(admin_id),

    CONSTRAINT uq_issue_unit UNIQUE (unit_id)
);

CREATE TABLE AUDIT_LOG (
    log_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    admin_id NUMBER,
    action VARCHAR2(100),
    table_name VARCHAR2(50),
    record_id NUMBER,
    log_time DATE DEFAULT SYSDATE,

    CONSTRAINT fk_log_admin
    FOREIGN KEY (admin_id)
    REFERENCES USER_ADMIN(admin_id)
);


