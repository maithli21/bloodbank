CREATE OR REPLACE PROCEDURE register_donor (
    p_name    IN VARCHAR2,
    p_age     IN NUMBER,
    p_gender  IN VARCHAR2,
    p_group   IN VARCHAR2,
    p_phone   IN VARCHAR2,
    p_email   IN VARCHAR2,
    p_address IN VARCHAR2
)
AS
BEGIN
    -- Check age
    IF p_age < 18 OR p_age > 65 THEN
        DBMS_OUTPUT.PUT_LINE('Age must be between 18 and 65');
        RETURN;
    END IF;

    -- Insert donor
    INSERT INTO DONOR
    (name, age, gender, blood_group, phone, email, address, last_donation_date)
    VALUES
    (p_name, p_age, p_gender, p_group, p_phone, p_email, p_address, SYSDATE);

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('Donor registered successfully');

EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        DBMS_OUTPUT.PUT_LINE('Phone number already exists');

    WHEN OTHERS THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('Error occurred while registering donor');
END;
/
