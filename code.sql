CREATE OR REPLACE PROCEDURE approve_request (
    p_request_id IN NUMBER
)
AS
    v_status BLOOD_REQUEST.status%TYPE;
BEGIN
    -- Get current request status
    SELECT status
    INTO v_status
    FROM BLOOD_REQUEST
    WHERE request_id = p_request_id;

    -- Only pending requests can be approved
    IF v_status != 'Pending' THEN
        DBMS_OUTPUT.PUT_LINE('Request is not pending');
        RETURN;
    END IF;

    -- Approve request
    UPDATE BLOOD_REQUEST
    SET status = 'Approved'
    WHERE request_id = p_request_id;

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('Request approved successfully');

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Invalid Request ID');

    WHEN OTHERS THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('Error while approving request');
END;
/

